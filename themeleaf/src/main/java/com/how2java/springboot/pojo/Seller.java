package com.how2java.springboot.pojo;

public class Seller {

	private int    slid;
	private String slna;
	private String slpa;
	private String sname;
	
	public Seller() {
		super();
	}

	public int getSlid() {
		return slid;
	}

	public void setSlid(int slid) {
		this.slid = slid;
	}

	public String getSlna() {
		return slna;
	}

	public void setSlna(String slna) {
		this.slna = slna;
	}

	public String getSlpa() {
		return slpa;
	}

	public void setSlpa(String slpa) {
		this.slpa = slpa;
	}

	public String getSname() {
		return sname;
	}

	public void setSname(String sname) {
		this.sname = sname;
	}
	
	
	
}
