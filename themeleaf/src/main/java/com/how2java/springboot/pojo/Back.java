package com.how2java.springboot.pojo;

public class Back {
	
	private int tiid;
	private int slid;
	private String backprice;
	public Back() {
		super();
	}
	public int getTiid() {
		return tiid;
	}
	public void setTiid(int tiid) {
		this.tiid = tiid;
	}
	public int getSlid() {
		return slid;
	}
	public void setSlid(int slid) {
		this.slid = slid;
	}
	public String getBackprice() {
		return backprice;
	}
	public void setBackprice(String backprice) {
		this.backprice = backprice;
	}
	

}
